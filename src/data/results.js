export function calculateResult(answers) {
  const counts = { A: 0, B: 0, C: 0 };

  answers.forEach(answer => {
    if (counts.hasOwnProperty(answer)) {
      counts[answer]++;
    }
  });

  console.log('Answer counts:', counts); // 디버깅용

  // 가장 많이 선택된 유형 찾기
  let maxType = 'B'; // 기본값은 중간
  let maxCount = -1;

  Object.entries(counts).forEach(([type, count]) => {
    if (count > maxCount) {
      maxCount = count;
      maxType = type;
    }
  });

  console.log('Selected type:', maxType); // 디버깅용

  return results[maxType];
}
